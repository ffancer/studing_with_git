def broken(inp):
    s = ''

    for i in inp:
        if i == '1':
            s += '0'
        else:
            s += '1'

    return s



print(broken('001111110011001110101111101001'), '110000001100110001010000010110')
print(broken('00110100110010011100110010100111100000011'), '11001011001101100011001101011000011111100')
print(broken('01001000010010011110100010010110111011100101101011100001110100101110001110010111'))
