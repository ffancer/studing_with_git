def move_ten(st):
    s = ''

    for i in st:
        s += chr(ord(i) + 10)

    return s


print(move_ten("testcase"), "docdmkco")
print(move_ten("codewars"), "mynogkbc")
print(move_ten("exampletesthere"), "ohkwzvodocdrobo")
